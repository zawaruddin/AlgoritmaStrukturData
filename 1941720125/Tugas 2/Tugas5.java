public class Tugas5 {
    int tinggi,panjang,sisiMiring;
    
    int hitungLuas(int a){
        a = tinggi*sisiMiring;
        return a ;
    }
    int hitungKeliling(int a){
        a = tinggi+panjang+sisiMiring;
        return a ;
    }
}

